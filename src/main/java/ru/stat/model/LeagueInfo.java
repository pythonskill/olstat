public class LeagueInfo {


    private String leagueName;
    private String leagueTierName;
    private String leagueDivision;

    public String getLeagueName() {
        return leagueName;
    }

    public void setLeagueName(String leagueName) {
        this.leagueName = leagueName;
    }

    public String getLeagueTierName() {
        return leagueTierName;
    }

    public void setLeagueTierName(String leagueTierName) {
        this.leagueTierName = leagueTierName;
    }

    public String getLeagueDivision() {
        return leagueDivision;
    }

    public void setLeagueDivision(String leagueDivision) {
        this.leagueDivision = leagueDivision;
    }
}
